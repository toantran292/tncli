// CopyFiles copies files from repo to worktree based on patterns.
func CopyFiles(repoDir, worktreeDir string, patterns []string) {
	for _, pattern := range patterns {
		if strings.Contains(pattern, "*") {
			matches := globFiles(repoDir, pattern)
			for _, relPath := range matches {
				copySingleFile(filepath.Join(repoDir, relPath), filepath.Join(worktreeDir, relPath))
			}
		} else {
			copySingleFile(filepath.Join(repoDir, pattern), filepath.Join(worktreeDir, pattern))
		}
	}
}

func copySingleFile(src, dest string) {
	info, err := os.Stat(src)
	if err != nil || info.IsDir() {
		return
	}
	if _, err := os.Stat(dest); err == nil {
		return // already exists
	}
	_ = os.MkdirAll(filepath.Dir(dest), 0o755)
	data, err := os.ReadFile(src)
	if err == nil {
		_ = os.WriteFile(dest, data, info.Mode())
	}
}

// ApplyEnvOverridesToDir writes env overrides to a specific directory.
func ApplyEnvOverridesToDir(dir string, envOverrides []EnvVar, envFileName string) {
	if len(envOverrides) == 0 {
		return
	}
	lines := []string{"# Auto-generated by tncli. Do not commit."}
	for _, ev := range envOverrides {
		lines = append(lines, ev.Key+"="+ev.Value)
	}
	_ = os.MkdirAll(dir, 0o755)
	_ = os.WriteFile(filepath.Join(dir, envFileName), []byte(strings.Join(lines, "\n")+"\n"), 0o644)
}

// ApplyEnvOverrides writes ALL env overrides to env file.
// When skipSubdirs is provided, those app subdirs are skipped (handled by per-service env).
func ApplyEnvOverrides(worktreeDir string, envOverrides []EnvVar, envFileName string, skipSubdirs ...string) {
	if len(envOverrides) == 0 {
		return
	}
	skipSet := make(map[string]bool)
	for _, s := range skipSubdirs {
		abs := filepath.Join(worktreeDir, s)
		skipSet[filepath.Clean(abs)] = true
	}
	for _, dir := range findEnvDirs(worktreeDir) {
		if skipSet[filepath.Clean(dir)] {
			continue
		}
		lines := []string{"# Auto-generated by tncli. Do not commit."}
		for _, ev := range envOverrides {
			lines = append(lines, ev.Key+"="+ev.Value)
		}
		_ = os.WriteFile(filepath.Join(dir, envFileName), []byte(strings.Join(lines, "\n")+"\n"), 0o644)
	}
}

func findEnvDirs(worktreeDir string) []string {
	var dirs []string
	if fileExists(filepath.Join(worktreeDir, ".env")) || fileExists(filepath.Join(worktreeDir, ".env.development")) {
		dirs = append(dirs, worktreeDir)
	}
	entries, err := os.ReadDir(filepath.Join(worktreeDir, "apps"))
	if err == nil {
		for _, e := range entries {
			if e.IsDir() {
				appDir := filepath.Join(worktreeDir, "apps", e.Name())
				if fileExists(filepath.Join(appDir, ".env")) {
					dirs = append(dirs, appDir)
				}
			}
		}
	}
	return dirs
}

func collectEnvKeys(dir, excludeFile string) map[string]bool {
	keys := make(map[string]bool)
	entries, err := os.ReadDir(dir)
	if err != nil {
		return keys
	}
	for _, e := range entries {
		name := e.Name()
		if !strings.HasPrefix(name, ".env") || name == excludeFile || e.IsDir() {
			continue
		}
		data, err := os.ReadFile(filepath.Join(dir, name))
		if err != nil {
			continue
		}
		for _, line := range strings.Split(string(data), "\n") {
			line = strings.TrimSpace(line)
			if line == "" || strings.HasPrefix(line, "#") {
				continue
			}
			if key, _, ok := strings.Cut(line, "="); ok {
				keys[strings.TrimSpace(key)] = true
			}
		}
	}
	return keys
}


// EnsureGlobalGitignore sets up global gitignore for tncli files.
func EnsureGlobalGitignore() {
	home, err := os.UserHomeDir()
	if err != nil {
		return
	}
	gitignorePath := filepath.Join(home, ".gitignore_global")

	excludes := ""
	if out, err := exec.Command("git", "config", "--global", "core.excludesfile").Output(); err == nil {
		excludes = strings.TrimSpace(string(out))
	}

	if excludes == "" {
		_ = exec.Command("git", "config", "--global", "core.excludesfile", gitignorePath).Run()
	}

	target := gitignorePath
	if excludes != "" {
		target = excludes
	}
	content, _ := os.ReadFile(target)
	contentStr := string(content)

	tncliFiles := []string{
		"docker-compose.shared.yml",
		".env.local",
		".env.*.local",
		"pnpm-lock.yaml",
		".tncli-workspace.json",
	}
	var toAdd []string
	for _, f := range tncliFiles {
		if !strings.Contains(contentStr, f) {
			toAdd = append(toAdd, f)
		}
	}
	if len(toAdd) > 0 {
		f, err := os.OpenFile(target, os.O_CREATE|os.O_APPEND|os.O_WRONLY, 0o644)
		if err != nil {
			return
		}
		defer f.Close()
		if !strings.Contains(contentStr, "# tncli") {
			fmt.Fprintln(f, "\n# tncli worktree")
		}
		for _, file := range toAdd {
			fmt.Fprintln(f, file)
		}
	}
}

// globFiles safely expands a glob pattern relative to repoDir using filepath.Glob.
func globFiles(repoDir, pattern string) []string {
	fullPattern := filepath.Join(repoDir, pattern)
	matches, err := filepath.Glob(fullPattern)
	if err != nil {
		return nil
	}
	var result []string
	for _, m := range matches {
		rel, err := filepath.Rel(repoDir, m)
		if err == nil && !strings.HasPrefix(rel, "..") {
			result = append(result, rel)
		}
	}
	return result
}

